-- =========================
-- DATABASE
-- =========================
CREATE DATABASE IF NOT EXISTS spk_siswa;
USE spk_siswa;

-- =========================
-- HAPUS TABEL LAMA
-- =========================
DROP TABLE IF EXISTS hasil_saw;
DROP TABLE IF EXISTS siswa;
DROP TABLE IF EXISTS kriteria;

-- =========================
-- TABEL SISWA
-- =========================
CREATE TABLE siswa (
  id INT AUTO_INCREMENT PRIMARY KEY,
  kode VARCHAR(10),
  nama VARCHAR(100),
  kehadiran FLOAT,
  terlambat INT,
  pelanggaran INT,
  sikap FLOAT
);

-- =========================
-- TABEL KRITERIA
-- =========================
CREATE TABLE kriteria (
  id INT AUTO_INCREMENT PRIMARY KEY,
  nama VARCHAR(50),
  bobot FLOAT,
  tipe ENUM('benefit','cost')
);

-- =========================
-- TABEL HASIL SAW
-- =========================
CREATE TABLE hasil_saw (
  id INT AUTO_INCREMENT PRIMARY KEY,
  siswa_id INT,
  skor FLOAT,
  ranking INT,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY (siswa_id) REFERENCES siswa(id)
);

